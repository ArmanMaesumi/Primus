package arman.edu.utexas.cs.Primus.utils;

public class Pair {

    private int L;
    private int R;

    public Pair (int L, int R){
        this.L = L;
        this.R = R;
    }

    public int getL() {
        return L;
    }

    public void setL(int l) {
        L = l;
    }

    public int getR() {
        return R;
    }

    public void setR(int r) {
        R = r;
    }
}
